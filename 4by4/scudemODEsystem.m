y(1)*(1-y(1))*(A(1,1)*y(1)+A(1,2)*y(2)+A(1,3)*y(3)+A(1,4)*y(4)+alpha*(A(1,1)*y(5)+A(1,2)*y(6)+A(1,3)*y(7)+A(1,4)*y(8)));
